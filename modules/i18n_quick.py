_current_lang = "de"

def set_language(lang: str):
    global _current_lang
    if lang in _STRINGS:
        _current_lang = lang

def get(key: str) -> str:
    return _STRINGS.get(_current_lang, _STRINGS["de"]).get(key, key)

def available_languages():
    return list(_STRINGS.keys())
